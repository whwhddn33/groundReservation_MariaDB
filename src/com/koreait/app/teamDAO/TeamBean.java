package com.koreait.app.teamDAO;

public class TeamBean {
	private int teamNo;
	private String teamName;
	private String captainName;
	private String teamTel;
	private String teamLocal;
	private String teamFoundYear;
	private String teamLevel;
	private String startAge;
	private String stopAge;
	private String uniformColor;
	private String uniformAway;
	private String controlLabel;
	
	public int getTeamNo() {
		return teamNo;
	}
	public void setTeamNo(int teamNo) {
		this.teamNo = teamNo;
	}
	public String getTeamName() {
		return teamName;
	}
	public void setTeamName(String teamName) {
		this.teamName = teamName;
	}
	public String getCaptainName() {
		return captainName;
	}
	public void setCaptainName(String captainName) {
		this.captainName = captainName;
	}
	public String getTeamTel() {
		return teamTel;
	}
	public void setTeamTel(String teamTel) {
		this.teamTel = teamTel;
	}
	public String getTeamLocal() {
		return teamLocal;
	}
	public void setTeamLocal(String teamLocal) {
		this.teamLocal = teamLocal;
	}
	public String getTeamFoundYear() {
		return teamFoundYear;
	}
	public void setTeamFoundYear(String teamFoundYear) {
		this.teamFoundYear = teamFoundYear;
	}
	public String getTeamLevel() {
		return teamLevel;
	}
	public void setTeamLevel(String teamLevel) {
		this.teamLevel = teamLevel;
	}
	public String getStartAge() {
		return startAge;
	}
	public void setStartAge(String startAge) {
		this.startAge = startAge;
	}
	public String getStopAge() {
		return stopAge;
	}
	public void setStopAge(String stopAge) {
		this.stopAge = stopAge;
	}
	public String getUniformColor() {
		return uniformColor;
	}
	public void setUniformColor(String uniformColor) {
		this.uniformColor = uniformColor;
	}
	public String getUniformAway() {
		return uniformAway;
	}
	public void setUniformAway(String uniformAway) {
		this.uniformAway = uniformAway;
	}
	public String getControlLabel() {
		return controlLabel;
	}
	public void setControlLabel(String controlLabel) {
		this.controlLabel = controlLabel;
	}
}
	
