package com.koreait.app.teamDAO;

public class TeamListBean {
	private int teamNo;
	private String username;
	private String nickname;
	private String userheight;
	private String userweight;
	private String usefoot;
	private String mainposition;
	private String minorposition;
	private String usertel;
	
	
	public int getTeamNo() {
		return teamNo;
	}
	public void setTeamNo(int teamNo) {
		this.teamNo = teamNo;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getUserheight() {
		return userheight;
	}
	public void setUserheight(String userheight) {
		this.userheight = userheight;
	}
	public String getUserweight() {
		return userweight;
	}
	public void setUserweight(String userweight) {
		this.userweight = userweight;
	}
	public String getUsefoot() {
		return usefoot;
	}
	public void setUsefoot(String usefoot) {
		this.usefoot = usefoot;
	}
	public String getMainposition() {
		return mainposition;
	}
	public void setMainposition(String mainposition) {
		this.mainposition = mainposition;
	}
	public String getMinorposition() {
		return minorposition;
	}
	public void setMinorposition(String minorposition) {
		this.minorposition = minorposition;
	}
	public String getUsertel() {
		return usertel;
	}
	public void setUsertel(String usertel) {
		this.usertel = usertel;
	}
	
	
}
